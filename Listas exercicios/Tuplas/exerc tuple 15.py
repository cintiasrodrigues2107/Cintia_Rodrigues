# Criando a tupla com os dias da semana
dias = ("Segunda", "Terça", "Quarta", "Quinta", "Sexta", "Sábado", "Domingo")

# Exibindo todos os dias
print("Dias da semana:", dias)

# Exibindo o primeiro dia
print("Primeiro dia:", dias[0])

# Exibindo o último dia
print("Último dia:", dias[-1])

# Exibindo o tamanho da tupla
print("Quantidade de dias:", len(dias))