# Leia n (int). Faça n += 3, n *= 2, n -= 4, n //= 3. Mostre cada etapa usando a função print().

n = int(input("Qual o valor de n: "))
n += 3
print("Qual valor do n + 3: " , n)

n *= 2
print("Qual valor do n * 2: " , n)

n -= 4
print("Qual valor do n - 4: " , n)

n //= 3
print("Qual valor do n // 3: " , n)
